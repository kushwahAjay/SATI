package com.qspiders.Arraysdemo;

public class Demo2d101 {

	public static void main(String[] args) {
		int arr[][]=new int [4][3];
		System.out.println(arr.length);//gives no of rows
		System.out.println(arr[0].length);//gives cols of row 1
		System.out.println(arr[1].length);//cols of row 2
		System.out.println(arr[2].length);//cols of row 3
		System.out.println(arr[3].length);//cols of row 4
	}

}
